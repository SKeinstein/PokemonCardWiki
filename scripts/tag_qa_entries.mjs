// ── 〜のポケモン: detect from card names in cards[] array ────────────────────
const CHARACTER_PREFIXES = [
  'MC', 'N', 'アイリス', 'アオキ', 'アクロマ', 'アセロラ', 'アンズ',
  'エリカ', 'カスミ', 'ギーマ', 'クセロシキ', 'シトロン', 'シロナ', 'スイレン',
  'ゼロ', 'タケシ', 'ダイゴ', 'ナンジャモ', 'ヒビキ', 'ベル', 'ペパー', 'ホップ',
  'ボス', 'マチス', 'マツバ', 'マリィ', 'ミカン', 'ミツル', 'メイ', 'リーリエ', 'ルチア',
  'ポケモンセンター', '暗号マニア', '探検家',
];

function detectCharacterTags(entry) {
  const tags = [];
  // Check both the Q&A text and the referenced card names
  const allNames = (entry.cards || []).map(c => c.name || '');
  const text = qaText(entry);
  for (const ch of CHARACTER_PREFIXES) {
    const prefix = ch + 'の';
    if (allNames.some(n => n.startsWith(prefix)) || text.includes(`「${prefix}`) || text.includes(`「${ch}`)) {
      tags.push('〜のポケモン', `〜のポケモン>${ch}`);
    }
  }
  // ロケット団 (handled by text rule above, but also check card names)
  if (allNames.some(n => n.startsWith('ロケット団の'))) {
    tags.push('特殊連動', '特殊連動>ロケット団');
  }
  return tags;
}

// ── Tag assignment ────────────────────────────────────────────────────────────
function assignTags(entry) {
  const tags = new Set();
  const t = qaText(entry);
  for (const rule of QA_TAG_RULES) {
    if (rule.cond(t)) {
      for (const tag of rule.tags) tags.add(tag);
    }
  }
  for (const tag of detectCharacterTags(entry)) tags.add(tag);
  return [...tags].sort();
}

