const splitClauses = (text) => text.split(/[。\n]/);
const matchSelfClause = (text, re) => splitClauses(text).some(s => !/相手/.test(s) && re.test(s));
const matchOppClause  = (text, re) => splitClauses(text).some(s =>  /相手/.test(s) && re.test(s));

