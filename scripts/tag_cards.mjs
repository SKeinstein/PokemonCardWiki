// ─── Tag assignment ───────────────────────────────────────────────────────────
function assignTags(c) {
  const tags = new Set();
  const add  = (...ts) => ts.forEach(t => tags.add(t));

  const all  = allText(c);
  const atk  = attackText(c);
  const abl  = abilityText(c);
  const rul  = rulesText(c);
  const name = c.name;

  for (const rule of TAG_RULES) {
    if (rule.condition(all, atk, abl, name, rul, c)) {
      add(...[rule.tags].flat());
    }
  }

  return [...tags].sort();
}

